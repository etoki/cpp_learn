#include <stdio.h>


//-------------------------------------------------------------------
// main
int
main()
{
    float A[100], B[100], C[100];

    // initialize array
    for(int i=0; i<100;i++)
    {
        A[i]=(float)(i+1000);
        B[i]=(float)i/10.f;
    }

    // calc.
    for(int i = 0; i < sizeof C/sizeof C[0]; i++)
    {
        C[i]=A[i]*B[i];
    }

    // list results
    printf("(A * B = C)\n");
    for(int i = 0; i < 10; i++)
        printf("%f * %f = %f\n", A[i], B[i], C[i]);

    return 0;
}
